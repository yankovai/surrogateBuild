class Gauss_Patterson:
    """
    Contains information about Gauss-Patterson abscissas.
    """

    def __init__(self,level,knots):
        """
        level = the degree of abscissas, which define everything else.
        knots = Gauss-Patterson abscissas for level
        nknots = number of knots
        bweights = barycentric weights for knots
        """
        
        self.level = level
        self.knots = knots
        self.nknots = len(knots)

    def _calculate_barycentric_weights(self):
        """
        Calculate barycentric weights for the Gauss-Patterson abscissas. Using
        Eq. 3.2 from the paper "Barycentric Lagrange Interpolation" by JP Berrut
        and LN Trefethen.
        """
        
        x = self.knots
        bweights = np.zeros(self.nknots)

        if self.level > 1:
            for i in range(0,self.nknots):
                xmask = np.zeros(self.nknots)
                xmask[i] = 1
                bweights[i] = 1./np.prod(x[i] - np.ma.array(x,mask=xmask))    
            bweights /= max(abs(bweights))
        else:
            bweights = np.array([1.])
            
        self.bweights = bweights
            
def gp_data_main():
    """
    Returns the dict object 'gp_data', which contains all abscissa information
    for the Gauss-Patterson schemem. Includes barycentric weights.
    """
    
    gp_knots = process_gp_knots()
    gp_data = {}
    for level in range(1,10):
        gp_data.setdefault(level)
        if level < 6: 
            gp_data[level] = Gauss_Patterson(level,gp_knots[level])
            gp_data[level]._calculate_barycentric_weights()
        else:
            gp_data[level] = None
            
    return gp_data

